package org.c3s.edgo.event.impl.beans.intl.missions;

public class Effect {

	private String Effect;
	private String Effect_Localised;
	private String Trend;
	
	public String getEffect() {
		return Effect;
	}
	public void setEffect(String effect) {
		Effect = effect;
	}
	public String getEffect_Localised() {
		return Effect_Localised;
	}
	public void setEffect_Localised(String effect_Localised) {
		Effect_Localised = effect_Localised;
	}
	public String getTrend() {
		return Trend;
	}
	public void setTrend(String trend) {
		Trend = trend;
	}

	
}
